# Algoritmo que pida dos números e indique si el primero es mayor que el segundo.


num1 = int(input("Numero 1: "))
num2 = int(input("Numero 2: "))

if num1 > num2 :
    print("El número 1 es mayor que el 2")
elif num1 < num2 :
    print("El número 1 es menor que el 2")
else :
    print("Tienen el mismo valor")
    